						<h3><a name="1"></a>La Gobernacion</h3>
						<p class="texto"><a name="1.1"></a><strong>Visión:</strong> Ser un gobierno modelo, eficaz y eficiente que contribuya al mejoramiento del nivel de vida integral de la población yaracuyana, a través de estrategias que conlleven al socialismo como modelo de desarrollo económico social, con proyección regional, nacional e internacional.</p>
						<p class="texto"><a name="1.2"></a><strong>Misión:</strong> impulsar el desarrollo sustentable en el Estado Yaracuy, “en el marco del Socialismo del Siglo XXI”, bajo el enfoque de la participación, protagonismo y poder popular; brindando servicios de calidad a través de políticas que fortalezcan la soberanía del estado, con respecto al pueblo, al ambiente, a la cultura y a la economía social bajo la figura del trabajador propietario, potenciando cada localidad para hacer de Yaracuy un Estado promisor, en el marco del proyecto revolucionario bolivariano del país.</p>
